//-----------------------CAL---------------------------
function myFunction1() {
  var checkBox = document.getElementById("checkforcal");
  var text = document.getElementById("CAL1");
  if (checkBox.checked == true){
    text.style.display = "block";
  } else {
     text.style.display = "none";
  }
}
